create database Employees;
use Employees;
create table employee(EmployeeID varchar(10), First_Name varchar(20), Last_Name varchar(20), Middle_Name varchar(20), DOB date, Gender char(1), Marital_Status char(3), Address varchar(100), Phone_Number int, Email varchar(30), Job_Title varchar(20), Salary decimal(10,2), DepartmentID varchar(20), ManagerID varchar(20), primary key(EmployeeID));
create table Department(DepartmentID varchar(10), Department_Name varchar(30), ManagerID varchar(10), primary key(DepartmentID));
create table Attendence(AttendenceID varchar(10), EmployeeID varchar(10), Date date, attendence varchar(10), primary key(AttendenceID));
create table Break(LeaveID varchar(10), EmployeeID varchar(10), Leave_Type varchar(20), From_Date date, To_Date date, leave_Status varchar(15), Reason varchar(100), primary key(LeaveID));
create table Benifits(BenefitID varchar(10), Benefit_Type varchar(20), Benefit_Description varchar(100), Eligibility_Criteria varchar(100), primary key(BenefitID));
create table login(Username varchar(30), Passkey varchar(30), primary key(Username));
create table project(ProjectID varchar(10), Project_Name varchar(20), Start_Date date, End_date date, Analysis varchar(100), primary key(ProjectID));
create table Salaries(SalaryID varchar(10), EmployeeID varchar(10), Salary decimal(10,2), Effective_Date date, primary key(SalaryID));
